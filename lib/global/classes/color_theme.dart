  Color primaryColor;
  Color secondaryColor;
  Color fontColor;
  Color secondaryFontColor;
  Color shadowColor;

  CustomColorTheme({
    required this.primaryColor,
    required this.secondaryColor,
    required this.fontColor,
    required this.secondaryFontColor,
    required this.shadowColor,
  });
}
